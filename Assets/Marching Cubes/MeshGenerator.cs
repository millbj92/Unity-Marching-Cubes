	void CreateBuffers()
    {
		int numPoints = numPointsPerAxis * numPointsPerAxis * numPointsPerAxis;
		int numVoxelsPerAxis = numPointsPerAxis - 1;
		int numVoxels = numVoxelsPerAxis * numVoxelsPerAxis * numVoxelsPerAxis;
		int maxTriangleCount = numVoxels * 5;

		// Always create buffers in editor (since buffers are released immediately to prevent memory leak)
		// Otherwise, only create if null or if size has changed
		if (!Application.isPlaying || (pointsBuffer == null || numPoints != pointsBuffer.count))
		{
			if (Application.isPlaying)
			{
				ReleaseBuffers();
			}
			triangleBuffer = new ComputeBuffer(maxTriangleCount, sizeof(float) * 3 * 3, ComputeBufferType.Append);
			pointsBuffer = new ComputeBuffer(numPoints, sizeof(float) * 4);
			triCountBuffer = new ComputeBuffer(1, sizeof(int), ComputeBufferType.Raw);

		}
	}

	void ReleaseBuffers()
	{
		if (triangleBuffer != null)
		{
			triangleBuffer.Release();
			pointsBuffer.Release();
			triCountBuffer.Release();
		}
	}

	public bool Init()
    {
		if(MarchingCubeManager.instance.MarchingCubesShader == null)
        {
			Debug.LogError("Unable to find Marching Cube Compute Shader. Exiting.");
			return false;
        }
		MarchingCubeGeneration = MarchingCubeManager.instance.MarchingCubesShader;
		smoothTerrain = MarchingCubeManager.instance.SmoothTerrain;
		flatShaded = MarchingCubeManager.instance.FlatShaded;
		generateCollider = MarchingCubeManager.instance.GenerateCollider;
		terrainHeight = MarchingCubeManager.instance.TerrainHeight;
		noiseThreshold = MarchingCubeManager.instance.NoiseThreshold;
		density = MarchingCubeManager.instance.Density;
		numPointsPerAxis = MarchingCubeManager.instance.NumPointsPerAxis;
		seed = MarchingCubeManager.instance.seed;
		width = (int)MarchingCubeManager.instance.ChunkSize;
		height = (int)MarchingCubeManager.instance.ChunkSize;
		length = (int)MarchingCubeManager.instance.ChunkSize;

		for (int i = 0; i < CornerTable.Length; i++)
		{
			CornerTable[i] *= MarchingCubeManager.instance.CellSize;
		}

		terrainMap = new float[width + MarchingCubeManager.instance.CellSize, height + MarchingCubeManager.instance.CellSize, length + MarchingCubeManager.instance.CellSize];

		meshFilter = GetComponent<MeshFilter>();
		meshCollider = GetComponent<MeshCollider>();
		transform.tag = "Terrain";

		return true;
	}

	public void CreateMesh()
    {
		if (!Init())
			return;

		PopulateTerrainMap();
		CreateMeshData();
		BuildMesh();
	}

	int GetCubeConfiguration(float[] cube)
	{
		int configurationIndex = 0;
		for (int i = 0; i < 8; i++)
		{
			if (cube[i] > noiseThreshold)
				configurationIndex |= 1 << i;
		}

		return configurationIndex;
	}

	void CreateMeshData()
	{
		ClearMeshData();

		for (int x = 0; x < width; x += MarchingCubeManager.instance.CellSize)
		{
			for (int y = 0; y < height; y += MarchingCubeManager.instance.CellSize)
			{
				for (int z = 0; z < length; z += MarchingCubeManager.instance.CellSize)
				{
					MarchCube(new Vector3Int(x, y, z));
				}
			}
		}
	}

	void PopulateTerrainMap()
	{
		for (int x = 0; x < width + MarchingCubeManager.instance.CellSize; x += MarchingCubeManager.instance.CellSize)
		{
			for (int y = 0; y < height + MarchingCubeManager.instance.CellSize; y += MarchingCubeManager.instance.CellSize)
			{
				for (int z = 0; z < length + MarchingCubeManager.instance.CellSize; z += MarchingCubeManager.instance.CellSize)
				{
					float nx = (((float)x + transform.position.x) / (float)width) * density;
					float ny = (((float)y + transform.position.y) / (float)height) * density;
					float nz = (((float)z + transform.position.z) / (float)length) * density;


					float noise;
					if (((float)y + transform.position.y) <= lowestPoint)
						noise = 0f;
					else if (((float)y + transform.position.y) >= terrainHeight)
						noise = 1f;
					else
						noise = Noise3D(nx, ny, nz);

					terrainMap[x, y, z] = noise;
				}
			}
		}
	}

	public float Noise3D(float x, float y, float z)
	{
		var desert = new Gain(
			new RidgeNoise(seed)
			{
				OctaveCount = 8
			} * 0.6f, 0.4f);
		// hills use a simple pink noise. 
		var hills = new PinkNoise(seed)
		{
			OctaveCount = 5, // smooth hills (no higher frequencies)
			Persistence = 0.56f // but steep (changes rapidly in the frequencies we do have)
		};
		// weight decides, whether a given point is hills or desert
		// cached, as we'd need it to decide texture at every point
		Generator m_Weight = new CoherentNoise.Generation.Cache(new PinkNoise(seed)
		{
			Frequency = 0.75f,
			OctaveCount = 3
		}.ScaleShift(0.5f, 0.5f));
		// overall heightmap blends hills and deserts
		Generator m_Generator = desert.Blend(hills, m_Weight).ScaleShift(.5f, .5f);

		/*var m_Generator = new PinkNoise(seed)
		{
			Lacunarity = 1.2f,
			Frequency = 0.5f,
			Persistence = .56f
		}.ScaleShift(.5f, .5f);*/


		float height = m_Generator.GetValue(x, y, z);
		return height;
	}

	public float PerlinNoise3D(float x, float y, float z)
	{
		float AB = Mathf.PerlinNoise(x, y);
		float BC = Mathf.PerlinNoise(y, z);
		float AC = Mathf.PerlinNoise(x, z);

		float BA = Mathf.PerlinNoise(y, x);
		float CB = Mathf.PerlinNoise(z, y);
		float CA = Mathf.PerlinNoise(z, x);

		float ABC = AB + BC + AC + BA + CB + CA;

		return ABC / 6f;
	}

	void MarchCube(Vector3Int position)
	{
		float[] cube = new float[8];
		for (int i = 0; i < 8; i++)
		{
			cube[i] = SampleTerrain(position + CornerTable[i]);
		}

		int configIndex = GetCubeConfiguration(cube);

		if (configIndex == 0 || configIndex == 255)
			return;

		int edgeIndex = 0;
		for (int i = 0; i < 5; i++)
		{
			for (int p = 0; p < 3; p++)
			{
				int indice = TriangleTable[configIndex, edgeIndex];

				if (indice == -1)
					return;

				Vector3 vert1 = position + CornerTable[EdgeTable[indice, 0]];
				Vector3 vert2 = position + CornerTable[EdgeTable[indice, 1]];

				Vector3 vertPosition;
				if (smoothTerrain)
				{
					float vert1Sample = cube[EdgeTable[indice, 0]];
					float vert2Sample = cube[EdgeTable[indice, 1]];

					float difference = vert2Sample - vert1Sample;

					if (difference == 0)
						difference = noiseThreshold;
					else
						difference = (noiseThreshold - vert1Sample) / difference;

					vertPosition = vert1 + ((vert2 - vert1) * difference);
				}
				else
				{
					vertPosition = (vert1 + vert2) / 2f;
				}

				if (flatShaded)
				{
					vertices.Add(vertPosition);
					triangles.Add(vertices.Count - 1);
				}
				else
					triangles.Add(VertForIndice(vertPosition));

				edgeIndex++;
			}
		}
	}

	int VertForIndice(Vector3 vert)
	{
		if (vertices.Any(v => v == vert))
			return vertices.IndexOf(vert);


		vertices.Add(vert);
		return vertices.Count - 1;
	}


	float SampleTerrain(Vector3Int point)
	{
		return terrainMap[point.x, point.y, point.z];
	}


	void ClearMeshData()
	{
		vertices.Clear();
		triangles.Clear();
	}

	void BuildMesh()
	{
		Mesh mesh = new Mesh();
		mesh.vertices = vertices.ToArray();
		mesh.triangles = triangles.ToArray();
		mesh.RecalculateNormals();

		if (generateCollider)
			meshCollider.sharedMesh = mesh;

		meshFilter.mesh = mesh;
	}
